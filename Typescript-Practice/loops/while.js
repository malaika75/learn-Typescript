// Here's a concise definition for a "while" loop:
//"A 'while' loop is a programming construct that repeatedly executes a block of code as long as a specified condition evaluates to true."
var i = 1;
while (i < 10) {
    console.log(i);
    i++;
}
var a = 1;
while (a <= 5) {
    console.log(a);
    a++;
}
var b = 1;
while (b <= 50) {
    console.log("hello world" + b);
    b++;
}
var z = 1;
while (z <= 5) {
    if (z % 2 == 0) {
        console.log(z);
    }
    z++;
}
